            <div class="row guide-title"><h1>The Anatomy Of A Perfect Résumé</h1>
                <p class="text-e"> - By Novorésumé - </p></div>
            <div class="article-section"><h2>1. Header</h2>
                <p>Firstly, the focus should be on presenting your skills and personality traits but at the same time
                    remember that it is important not to lie or exaggerate the abilities that you do not posses. </p>
                <p>Always keep in mind that sometimes <b>“Less, Is More!”</b></p>
                <p class="p-near-ul">The first part of your résumé is the Header which needs to include the following
                    information:</p>
                <ul>
                    <li><p><b>Picture</b>: this is optional, depending on the country/culture you are applying.</p></li>
                    <li><p><b>Email Address</b>: your email should be just your name or some professional variations of
                        it.</p></li>
                    <li><p><b>Phone Number</b>: make sure to write the number where you are available most of the time.
                    </p></li>
                    <li><p><b>Address</b>: listing your City/Country is mandatory. This is especially needed when your
                        application is being scanned by an ATS software.</p></li>
                    <li><p><b>Social Media Profiles</b>: Make an audit of your social media profiles and be sure you do
                        not have any content that might create a bad impression about you.</p></li>
                </ul>
            </div>
            <div class="article-section"><h2>2. Work Experience</h2>
                <p>The first section of your résumé should be Work Experience in the case you have a couple of years of
                    related work experience for the job you are applying for.</p>
                <p class="p-near-ul">The <i>mandatory</i> information for this section is: </p>
                <ul>
                    <li><p>Employment Period.</p></li>
                    <li><p>Position/Title. </p></li>
                    <li><p>Name of the Company/Organisation. </p></li>
                    <li><p>Accomplishments/Responsibilities or Tasks.</p></li>
                </ul>
                <p class="p-near-ul">You may include as well the following <i>optional</i> information: </p>
                <ul>
                    <li><p>City/Country of the previous company. </p></li>
                    <li><p>Company Description. </p></li>
                    <li><p>Contact Info of a supervisor or colleague. </p></li>
                </ul>
                <p class="p-near-ul">General tips for Work Experience section include:</p>
                <ul>
                    <li><p>Switch the positions around your résumé if some particular experiences are more relevant to a
                        certain job you are applying for.</p></li>
                    <li><p>Include between 2-4 jobs that are relevant to the one you are applying for. </p></li>
                    <li><p>Try to begin each sentence with an action verb when you write the
                        Accomplishments/Responsibilities or Tasks. </p></li>
                    <li><p>Quantify your Accomplishments/Responsibilities or Tasks whenever this is possible. (E.g.:
                        “Increased customer base by 25% following a series of dedicated sales campaigns.”) </p></li>
                    <li><p>You need to make it clear in this section how your current work experience makes you the
                        ideal candidate for the position you are applying for. </p></li>
                    <li><p>you the ideal candidate for the position you are applying for. If you did not have any
                        Accomplishments in the previous job and decide to list previous tasks, remember not to list the
                        ones that are too general or vague, but rather focus on the specific ones. </p></li>
                </ul>
            </div>
            <div class="article-section"><h2>3. Professional Skills/Skills &amp; Competencies</h2>
                <p>The second most important section is “Skills &amp; Competencies.” Try to keep them targeted for the
                    position you are applying for. If for example, you are applying for a job as a Marketing Manager you
                    should not list a skill such as “breakdance” that is not relevant for this particular job. </p>
                <p class="p-near-ul">General tips for the Skills &amp; Competencies include the following: </p>
                <ul>
                    <li><p>Read the job ad many times and understand what skills are sought by the employer before
                        writing this section. Most of the time you will find the skills that are required for that
                        specific position in the ad. </p></li>
                    <li><p>Using industry or job specific skills in your résumé will enable your application to pass
                        through an applicant tracking system (ATS). </p></li>
                    <li><p>Search on the internet for the skills that are most needed in the industry you are applying
                        for a list the ones that you possess. </p></li>
                    <li><p>Include as well the “Soft Skills” that are demanded by almost all the jobs. E.g.:
                        Self-motivation, Communication, Time Management, Public Speaking, Leadership, Storytelling. </p>
                    </li>
                </ul>
            </div>
            <div class="article-section"><h2>4. Education</h2>
                <p>In case you are a student, recent graduate or do not have any work experience, we suggest to include
                    the “Education” section at the top of your résumé instead of Work Experience. Remember to arrange
                    this section in reverse order. If, for example, you have a Master’s and a Bachelor's Degree, make
                    sure that you list first your Master’s Degree. If you already have a Bachelor’s Degree, you should
                    not include your High School details. </p>
                <p class="p-near-ul">General tips for Education include the following: </p>
                <ul>
                    <li><p>Include the Universities/Colleges/Academies/Schools, the name of the program and the courses
                        that you took and are relevant to the position you are applying for. </p></li>
                    <li><p>Do NOT falsify anything when it comes to your education. It comes without saying, that if you
                        exaggerate or lie about it, somebody will eventually notice it. </p></li>
                </ul>
                <p>The following sections are optional, and you should include the ones that are relevant to the job you
                    are applying for and add value to your application: </p></div>
            <div class="article-section"><h2>5. Projects</h2>
                <ul>
                    <li><p>Include the most important personal project that you have worked on. These could be semester
                        projects or projects that you worked on in your own free time. </p></li>
                    <li><p>List the name of the project, a short description of what the project was about, what kind of
                        knowledge did you apply, and what skills did you develop while working on it. </p></li>
                </ul>
            </div>
            <div class="article-section"><h2>6. Languages</h2>
                <ul>
                    <li><p>When creating a professional résumé, the languages that you master can make you more
                        competitive. It will also show that you are open to change, learning new things and that you can
                        work with people outside your culture. </p></li>
                    <li><p>Include all the Languages that you know and the level of your knowledge. Knowing an uncommon
                        language can help you stand out from other candidates. </p></li>
                </ul>
            </div>
            <div class="article-section"><h2>7. Achievements and Certificates </h2>
                <p>List all your important Achievements and Certificates that you have obtained and are to some extent
                    related to the job you are applying for. </p>
                <p class="p-near-ul"><i>Examples of Achievements: </i></p>
                <ul>
                    <li><p>Employee of the Month - January and March 2016</p></li>
                    <li><p>Nominated as a team leader in various projects across 3 departments. </p></li>
                    <li><p>Finished “X” project 2 weeks before the deadline. </p></li>
                </ul>
                <br>
                <p class="p-near-ul"><i>Examples of Achievements: </i></p>
                <ul>
                    <li><p>Certified Professional Crisis Manager for ISO 292 Security Management (2011)</p></li>
                    <li><p>Risk Management course at Novoschool (2016) </p></li>
                    <li><p>CPL - Commercial Pilot Licence (2008) </p></li>
                </ul>
            </div>
            <div class="article-section"><h2>8. Volunteer Experience</h2>
                <p>For the recent graduates with minimal or no work experience there should be an emphasis on this
                    section, even make volunteerism a central part of your résumé.</p>
                <p>This section can highlight important skills, such as Communication, Leadership, and Planning while
                    showing as well that you are adaptable and self-motivated. </p>
                <p>You can also research the employer and see what causes they care about, then mirror those in your
                    volunteer experience if you had related experiences. </p></div>
            <div class="article-section"><h2>9. Honors and Awards </h2>
                <p>Along with mentioning your awards, you need to offer a short explanation of the meaning of that
                    award. It is important to separate and make a distinction between your professional and personal
                    awards. </p>
                <p>Do not include awards that are too old. For example, it is not recommended to include an award from
                    high-school when you already have 5-10 years of work experience. </p></div>
            <div class="article-section"><h2>10. Honors and Awards </h2>
                <p>It is important to include the conferences and professional courses you have completed, especially
                    the ones where you held a presentation or were part of the organizers. </p>
                <p>Conferences add value especially for the fresh graduates who lack work experience. </p></div>
            <div class="article-section"><h2>11. Supported Causes &amp; Interests</h2>
                <p>Including the causes you care about will offer the employer a glimpse of your personality. You should
                    be able to prove and talk about the causes you support if needed. </p>
                <p>If you have space left after writing the most important sections in your résumé, you can add an extra
                    section to list your interests. Listing them will show the employer a more personal part of you. In
                    some cultures, it is even mandatory to include this section. </p>
                <p>Remember to be short and concise, just mention the main activities that interest you but do not go
                    into details. If your interests stirred their curiosity, they would ask you more during the
                    interview, therefore be prepared to talk about them. </p></div>
            <div class="article-section"><h2>12. Checklist</h2>
                <div class="one-half-left"><h2 class="text-c">DO’s</h2>
                    <ul>
                        <li><p>Tailor your summary to include elements of the job description for each position you are
                            applying for. </p></li>
                        <li><p>Focus on specific results of your work, significant achievements and recognition
                            received. </p></li>
                        <li><p>Remember that “Less, is More!” Keep your résumé to one page if you do not have more than
                            5 years of relevant work experience. </p></li>
                        <li><p>Tailor your résumé for each specific job you are applying for. </p></li>
                    </ul>
                </div>
                <div class="one-half-right"><h2 class="text-d">DON’Ts </h2>
                    <ul>
                        <li><p>Submit the same résumé to every employer, regardless of the position. </p></li>
                        <li><p>Mention “salary negotiable” or “reference available upon request.” </p></li>
                        <li><p>Use acronyms or jargons. </p></li>
                        <li><p>Include routine job duties, such as: “making copies.” </p></li>
                        <li><p>Overuse the personal pronouns “I,” “me” or “my.” </p></li>
                        <li><p>Say you were laid off or fired from your last position. </p></li>
                    </ul>
                </div>
                <div class="row"><p class="p-near-ul">Do not submit your résumé before checking the points on the
                    following Checklist: </p>
                    <ul>
                        <li><p>Is your name, address, phone number and email address at the top of the page? </p></li>
                        <li><p>Can an employer understand the main sections within 7 to 10 seconds? </p></li>
                        <li><p>Did you write your résumé for each specific position by including the key skills and
                            experiences the employer wants? </p></li>
                        <li><p>Is the information listed in order of importance and relevance to the requirements listed
                            in the job description? </p></li>
                        <li><p>Do most phrases begin with an action verb, such as “conducted,” “implemented,”
                            “increased,” etc.? </p></li>
                        <li><p>Have you been accurate and truthful about your accomplishments rather than being too
                            modest or exaggerating? </p></li>
                        <li><p>Did you double check the spelling of every word and made sure the grammar and punctuation
                            are correct? </p></li>
                    </ul>
                </div>
            </div>
